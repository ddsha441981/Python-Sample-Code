'''
While Loop ----> Conditions check first and 
if condition true then loop execute and if condition is false then break the loop
'''
myList = ['A', 'B', 'C', 'D', 'E', 'F', 'G', 'H', 'I', 'J', 'K', 'L', 'M', 'N', ]
print(len(myList))
i = 0
while (i < len(myList)):
    print(myList[i])
    i = i+1
